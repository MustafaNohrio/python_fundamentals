#for loop using range
for num in range(10):
    print(num)

#range with starting point
print("\nrange with starting point")
for num in range(5, 10):
    print(num)

#range with starting point and step
print("\nrange with step")
for num in range(2, 10, 2):
    print(num)

#iterating over string
name = " mustafa"
for ch in name:
    print(ch)


